#!/usr/bin/env python3
"""Render Speakeasy's RELEASES.md as a Mintlify TypeScript SDK release log."""

from __future__ import annotations

import argparse
import re
from dataclasses import dataclass
from datetime import datetime
from pathlib import Path


FRONTMATTER = """---
title: "TypeScript SDK release log"
description: "Release history for the TextQL TypeScript SDK"
icon: "js"
---

Install the latest release from [npm](https://www.npmjs.com/package/@textql/sdk):

```bash
npm install @textql/sdk
```
"""

SECTION = re.compile(
    r"^## (?P<timestamp>\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2})\s*$",
    re.MULTILINE,
)
NPM_LINK = re.compile(
    r"\[NPM v(?P<version>[^\]]+)\](?:\((?P<link_url>https?://[^)]+)\)|\s+(?P<plain_url>https?://\S+))"
)
GENERATED_TYPESCRIPT = re.compile(r"\[typescript v(?P<version>[^\]]+)\]")
SPEAKEASY = re.compile(
    r"^- Speakeasy CLI (?P<cli>\S+)(?: \((?P<generator>[^)]+)\))?(?: (?P<url>https?://\S+))?\s*$",
    re.MULTILINE,
)


@dataclass(frozen=True)
class Release:
    published_at: datetime
    version: str
    npm_url: str
    speakeasy_cli: str | None = None
    generator_version: str | None = None
    speakeasy_url: str | None = None


def parse_releases(
    markdown: str, published_versions: set[str] | None = None
) -> list[Release]:
    """Parse the release records generated by Speakeasy."""
    matches = list(SECTION.finditer(markdown))
    if not matches:
        raise ValueError("RELEASES.md does not contain any timestamped releases")

    releases: list[Release] = []
    seen_versions: set[str] = set()
    for index, match in enumerate(matches):
        end = matches[index + 1].start() if index + 1 < len(matches) else len(markdown)
        body = markdown[match.end() : end]
        npm = NPM_LINK.search(body)
        generated = GENERATED_TYPESCRIPT.search(body)
        if npm is not None:
            version = npm.group("version")
            npm_url = npm.group("link_url") or npm.group("plain_url")
        elif (
            generated is not None
            and published_versions is not None
            and generated.group("version") in published_versions
        ):
            version = generated.group("version")
            npm_url = f"https://www.npmjs.com/package/@textql/sdk/v/{version}"
        else:
            # Speakeasy records successful generations before a package is
            # necessarily published. Those entries do not belong in the npm
            # release log yet.
            continue

        if version in seen_versions:
            continue
        seen_versions.add(version)

        speakeasy = SPEAKEASY.search(body)
        releases.append(
            Release(
                published_at=datetime.strptime(
                    match.group("timestamp"), "%Y-%m-%d %H:%M:%S"
                ),
                version=version,
                npm_url=npm_url,
                speakeasy_cli=speakeasy.group("cli") if speakeasy else None,
                generator_version=speakeasy.group("generator") if speakeasy else None,
                speakeasy_url=speakeasy.group("url") if speakeasy else None,
            )
        )
    if not releases:
        raise ValueError("RELEASES.md does not contain any published NPM releases")
    return sorted(releases, key=lambda release: release.published_at, reverse=True)


def render_release(release: Release) -> str:
    date = (
        f"{release.published_at.strftime('%B')} "
        f"{release.published_at.day}, {release.published_at.year}"
    )
    lines = [
        f'<Update label="v{release.version}" description="{date}">',
        "",
        f"[View `@textql/sdk` v{release.version} on npm]({release.npm_url}).",
        "",
        "```bash",
        f"npm install @textql/sdk@{release.version}",
        "```",
    ]
    if release.speakeasy_cli:
        label = f"Speakeasy CLI {release.speakeasy_cli}"
        if release.generator_version:
            label += f" (generator {release.generator_version})"
        if release.speakeasy_url:
            lines.extend(["", f"Generated with [{label}]({release.speakeasy_url})."])
        else:
            lines.extend(["", f"Generated with {label}."])
    lines.extend(["", "</Update>"])
    return "\n".join(lines)


def render(markdown: str, published_versions: set[str] | None = None) -> str:
    releases = parse_releases(markdown, published_versions)
    return FRONTMATTER.rstrip() + "\n\n" + "\n\n".join(
        render_release(release) for release in releases
    ) + "\n"


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("source", type=Path, help="Speakeasy RELEASES.md")
    parser.add_argument("destination", type=Path, help="Mintlify MDX output")
    parser.add_argument(
        "--published-version",
        action="append",
        default=[],
        help="Include a generated version known to be published on npm",
    )
    args = parser.parse_args()

    source = args.source.read_text()
    published_versions = set(args.published_version)
    releases = parse_releases(source, published_versions)
    args.destination.parent.mkdir(parents=True, exist_ok=True)
    args.destination.write_text(render(source, published_versions))
    print(f"Rendered {len(releases)} TypeScript SDK release(s)")


if __name__ == "__main__":
    main()
